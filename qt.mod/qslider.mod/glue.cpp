// ---------------------------------------------------------------------------------------

MaxQSlider::MaxQSlider(BBObject * handle, Qt::Orientation orientation, QWidget * parent)
	: maxHandle(handle), QSlider(orientation, parent)
{
	qbind(this, handle);
}

MaxQSlider::~MaxQSlider()
{
	qunbind(this);
}

void MaxQSlider::onActionTriggered(int action) {
	_qt_qabstractslider_QAbstractSlider__OnActionTriggered(maxHandle, action);
}

void MaxQSlider::onRangeChanged(int min, int max) {
	_qt_qabstractslider_QAbstractSlider__OnRangeChanged(maxHandle, min, max);
}

void MaxQSlider::onSliderMoved(int value) {
	_qt_qabstractslider_QAbstractSlider__OnSliderMoved(maxHandle, value);
}

void MaxQSlider::onSliderPressed() {
	_qt_qabstractslider_QAbstractSlider__OnSliderPressed(maxHandle);
}

void MaxQSlider::onSliderReleased() {
	_qt_qabstractslider_QAbstractSlider__OnSliderReleased(maxHandle);
}

void MaxQSlider::onValueChanged(int value) {
	_qt_qabstractslider_QAbstractSlider__OnValueChanged(maxHandle, value);
}

void MaxQSlider::onCustomContextMenuRequested(const QPoint & pos) {
	_qt_qwidget_QWidget__OnCustomContextMenuRequested(maxHandle, pos.x(), pos.y());
}


// *********************************************

QSlider * bmx_qt_qslider_create(BBObject * handle, int orientation, QWidget * parent) {
	return new MaxQSlider(handle, (Qt::Orientation)orientation, parent);
}

void bmx_qt_qslider_settickinterval(QSlider * slider, int interval) {
	slider->setTickInterval(interval);
}

void bmx_qt_qslider_settickposition(QSlider * slider, int position) {
	slider->setTickPosition((QSlider::TickPosition)position);
}

int bmx_qt_qslider_tickinterval(QSlider * slider) {
	return slider->tickInterval();
}

int bmx_qt_qslider_tickposition(QSlider * slider) {
	return slider->tickPosition();
}

